package edu.vitor.semana2;

/* 
 * Alguns tipos de dados
 */

public class TiposDados {
    public static void main(String[] args) {
        byte idade = 123; // Geralmente é utilizado int
        short ano = 2022; // Geralmente é utilizado int
        int cep = 12345678; // Se iniciar com zero, talvez precise ser de outro tipo
        long cpf = 12345678910L; // Se iniciar com zero, talvez precise ser de outro tipo | termina com "L" sempre 
        float pi = 3.14F; // Precisa terminar com "F" sempre
        double salario = 1234.56; // Mais utilizado que o tipo float
    }
}